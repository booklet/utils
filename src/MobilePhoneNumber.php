    public static function isCellPhoneNumber(int $phone_number)
    {
        if (strlen($phone_number) != 9) {
            return false;
        }

        foreach (self::MOBILE_NETWORK_PREFIXES as $prefix => $mobile_network_name) {
            if (substr($phone_number, 0, strlen($prefix)) == $prefix) {
                return true;
            }
        }

        return false;
    }

    // use to generate MOBILE_NETWORK_PREFIXES table from xml UKE file
    public static function importFormUKE()
    {
        $xml_string = file_get_contents('T2-PLMN_T9-MVNO_20.06.2017.xml');
        $xml = new SimpleXMLElement($xml_string);

        $nums = [];
        foreach ($xml->numery->plmn as $record) {
            $prefix = (string) $record->numer;
            $operator_name = (string) $record->operator;

            // '459(3,4,6-0)' => 'Rezerwa Prezesa UKE',
            if (StringUntils::isInclude($prefix, '(')) {
                $pre_prefix = explode('(', $prefix)[0];
                preg_match('#\((.*?)\)#', $prefix, $match);
                $ranges = explode(',', $match[1]); // 3,4,6-0

                foreach ($ranges as $range) {
                    if (StringUntils::isInclude($range, '-')) {
                        list($range_start, $range_stop) = explode('-', $range);
                        // hack for (6-0) range
                        if (intval($range_stop) == 0) {
                            $range_stop = 9;
                            $add_zero_range = true;
                        }
                        foreach (range($range_start, $range_stop) as $num) {
                            $full_prefix = intval($pre_prefix . $num);
                            $nums[$full_prefix] = $operator_name;
                        }
                        if (isset($add_zero_range)) {
                            $full_prefix = intval($pre_prefix . 0);
                            $nums[$full_prefix] = $operator_name;
                        }
                    } else {
                        $full_prefix = intval($pre_prefix . $range);
                        $nums[$full_prefix] = $operator_name;
                    }
                }
            } else {
                $nums[$prefix] = $operator_name;
            }
        }

        return $nums;
    }
